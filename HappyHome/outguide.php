			<font color="white"><u><h3>Accessibility of Outdoor Spaces and path Design</h3></u>
				<p>
					There should be easy access to garden spaces. Light, easily opened doors and minimal door thresholds make it easier for people to get outside.
					<br>
                    Having well-defined paths helps people to find their way around outdoor spaces. The research evidence suggests that free-flowing looped designs are preferred.
					<br>
					Well maintained paths within the garden help to minimise trip hazards.
					<br>
					People find garden tools which have been appropriately adapted easier to use. Example adaptations include, e.g. incorporating longer handles, using colour to draw attention to the tool or parts of the tool, having tactile guides.
					<br>
					It is important for outdoor areas to have appropriate lighting as this helps people to find their way around and encourages use of outdoor spaces. Different types of lighting may be used for different purposes, e.g. lighting under handrails could be used to highlight paths, and security lights could be used to provide widespread light after dark in an outdoor space.
					<br>
					Using contrast on external stairs and steps helps to highlight the change from a flat surface to steps, and using edging materials for paths which contrast with the surrounding area makes paths easier for people to identify and follow.
					<br>
					Contrast can help to highlight both key features and hazards in outdoor spaces. It is important that different contrasts are used so that people can clearly identify which is being highlighted in any given instance.
                </p>
			<font color="white"><u><h3>Plantings</h3></u>
				<p>
					Plants that make interesting sounds, e.g. bamboos and grasses that rustle, or seed pods that pop, and/or those that have pleasant or interesting smells and textures provide people with additional sensory stimulation.
					<br>
					People like to touch and feel things growing in their gardens so planting schemes which include poisonous plants and those likely to cause skin irritation should be avoided.
					<br>
					Large sections of small plants of the same colour may be easier for people to see than large plants of a single colour.
					<br>
					Warm colours (such as oranges, reds and yellows) may be easier for people to pick out than cooler colours (such as blues).
				</p>
